# conversão de tipos, coreção
# type convertion, tpecasting, coercion
# é o ato de converter um tipo em outro tipos
# imutáveis e primitivos
# int, float, str, bool

print(10 + 25)
print('10' + '25')
print(10 + int('25'))
print(str(10) + '25')
print(10 + float('25.5'))
print(float(10) + float('25.5'))
print(bool(0))
print(bool(1))
print(bool(10))
print(bool(-1))
print(bool(''))
print(bool('Hello, World!'))    