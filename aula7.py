# Variáveis são usadas para salvar algo na memória do computador.
# PAP8: inicie variáveis com letras minúsculas, pode usar numeros e underline.
# O sinal de = é o operador de atribuição, Ele é usado para atribuir um valor a uma variável (nome).
# Uso: nome_da_variavel = valor
# Exemplo:

idade = 25
nome = 'João'
altura = 1.75
print(idade)
print(nome)
print(altura)   
